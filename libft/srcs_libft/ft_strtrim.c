#include "h_libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*cad;
	size_t	i;

	if (!(set) || !(s1))
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	i = ft_strlen(s1);
	while (i && ft_strchr(set, s1[i]))
		i--;
	cad = ft_substr(s1, 0, i + 1);
	return (cad);
}
